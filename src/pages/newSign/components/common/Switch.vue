<template>
  <div :class="className" @click="onClick">
    <span class="open"></span>
    <span class="close"></span>
  </div>

</template>

<script>
'use strict';
export default {
  props: {
    value: {
      default: true
    },
    // blue red green orange
    color: {
      type: String,
      default: 'red'
    },
    openValue: {
      default: true
    },
    closeValue: {
      default: false
    },
    disabled: {
      type: Boolean,
      default: false
    }
  },
  computed: {
    className () {
      let {
        value,
        openValue,
        closeValue,
        color,
        disabled
      } = this;
      return {
        'vue-switch': true,
        'z-on': value === openValue,
        'z-disabled': disabled,
        ['c-' + color]: true
      };
    }
  },
  methods: {
    onClick () {
      let {
        disabled,
        value,
        openValue,
        closeValue
      } = this;
      if (!disabled) {
        if (openValue === value) {
          this.$emit('input', closeValue);
        } else {
          this.$emit('input', openValue);
        }
      }
    }
  }
}
</script>

